package com.example.demo.util;



public class FrameData {
    private int count;
    private double speed;

    public FrameData(int count, double speed) {
        this.count = count;
        this.speed = speed;
    }

    // Getter methods for count and speed
    public int getCount() {
        return count;
    }

    public double getSpeed() {
        return speed;
    }
}
